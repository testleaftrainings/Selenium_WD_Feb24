package week1.day2;

public class LearnLoopingStatement {

	public static void main(String[] args) {
		
		// syntax for(initialzation; conditional ; increment/decrement){}
		// int i=1;  i++  --> i+1 = 2
		for(int i=1; i<=10; i++) {
			System.out.println("Testleaf");
		}
		
		// i want to print number from 1 to 10
		for(int i=1; i<=10; i++) { //1<=10, 2<=10, 3<=10
			System.out.println(i);
		}
		
		

	}

}
